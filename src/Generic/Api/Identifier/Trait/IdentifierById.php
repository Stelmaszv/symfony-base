<?php


namespace App\Generic\Api\Identifier\Trait;

use Doctrine\ORM\Mapping as ORM;

trait IdentifierById
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(int $id): self
    {
        $this->id = $id;

        return $this;
    }
}